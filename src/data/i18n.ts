export const languages = [
  { code: "en", label: "English", short: "EN" },
  { code: "de", label: "Deutsch", short: "DE" },
  { code: "fr", label: "Français", short: "FR" },
  { code: "es", label: "Español", short: "ES" },
  { code: "it", label: "Italiano", short: "IT" },
  { code: "nl", label: "Nederlands", short: "NL" },
  { code: "pt", label: "Português", short: "PT" },
] as const;

export type Lang = (typeof languages)[number]["code"];

export const defaultLang: Lang = "en";

export const isLang = (value: string | undefined): value is Lang =>
  languages.some((language) => language.code === value);

export const stripLangPrefix = (pathname: string) => {
  const parts = pathname.split("/").filter(Boolean);
  if (isLang(parts[0])) {
    return `/${parts.slice(1).join("/")}${parts.length > 1 ? "/" : ""}`;
  }

  return pathname === "" ? "/" : pathname;
};

export const localizePath = (pathname: string, lang: Lang) => {
  const cleanPath = stripLangPrefix(pathname);
  if (lang === defaultLang) {
    return cleanPath || "/";
  }

  const suffix = cleanPath === "/" ? "" : cleanPath.replace(/^\//, "");
  return `/${lang}/${suffix}`;
};

